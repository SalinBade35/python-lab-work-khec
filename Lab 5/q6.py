# 6. Write a Python program to convert a given string list to a tuple. 
# Original string: python 3.0 
# <class 'str'> 
# Convert the said string to a tuple: 
# ('p', 'y', 't', 'h', 'o', 'n', '3', '.', '0') 
# <class 'tuple'>




original_string = "python 3.0"

print("Original string:", original_string)
print(type(original_string))

# Convert the string to a tuple
converted_tuple = tuple(original_string)

# Print the converted tuple and its type
print("Convert the said string to a tuple:")
print(converted_tuple)
print(type(converted_tuple))

    
    
    





